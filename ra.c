#include "push_swap.h"

void	ra(t_elem **a, t_elem **b)
{
	t_elem *e;
	t_elem *p;
	t_elem **bb;

	bb = b;
	if (a && *a && (*a)->next)
	{
		e = *a;
		*a = (*a)->next;
		e->next = NULL;
		p = *a;
		while (p->next)
			p = p->next;
		p->next = e;
	}
}
