#include "push_swap.h"

void	rb(t_elem **a, t_elem **b)
{
	t_elem *e;
	t_elem *p;
	t_elem **aa;

	aa = a;
	if (b && *b && (*b)->next)
	{
		e = *b;
		*b = (*b)->next;
		e->next = NULL;
		p = *b;
		while (p->next)
			p = p->next;
		p->next = e;
	}
}
