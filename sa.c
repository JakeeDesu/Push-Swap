#include "push_swap.h"

void	sa(t_elem **a, t_elem **b)
{
	t_elem *e1;
	t_elem **bb;

	bb = b;
	if (a && *a && (*a)->next)
	{
		e1 = *a;
		*a = (*a)->next;
		e1->next = (*a)->next;
		(*a)->next = e1;
	}
}
