#include "push_swap.h"

void	sb(t_elem **a, t_elem **b)
{
	t_elem *e1;
	t_elem **aa;

	aa = a;
	if (b && *b && (*b)->next)
	{
		e1 = *b;
		*b = (*b)->next;
		e1->next = (*b)->next;
		(*b)->next = e1;
	}
}
