#include "push_swap.h"

void	pa(t_elem **a, t_elem **b)
{
	t_elem *e;

	if (a && *a && b)
	{
		if (*b == NULL)
		{
			*b = *a;
			*a = (*a)->next;
			(*b)->next = NULL;
		}
		else
		{
			e = *a;
			*a = (*a)->next;
			e->next = *b;
			*b = e;
		}
	}
}
